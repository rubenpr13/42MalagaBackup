#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int		i;
	char	x;

	i = 0;
	while (str[i] != '\0')
	{
		x = str[i];
		if ((x < 'A') || (x > 'Z' && x < 'a') || (x > 'z'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
